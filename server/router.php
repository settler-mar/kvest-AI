$url = $_SERVER["REQUEST_URI"];
$url = 'http://127.0.0.1:8080'.$url.(strrpos($url,'?')?'&':'?').'ip='.getUserIP().'&command='.$command;

curl_setopt($ch, CURLOPT_URL, $url);
